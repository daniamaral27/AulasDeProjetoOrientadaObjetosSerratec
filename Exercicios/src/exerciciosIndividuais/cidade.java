package exerciciosIndividuais;

public class cidade {
 private String nomeCidade;
 private estado estado;
 
public cidade(String nomeCidade, estado estado) {
	super();
	this.nomeCidade = nomeCidade;
	this.estado = estado;
}

public String getNomeCidade() {
	return nomeCidade;
}

public estado getEstado() {
	return estado;
}
 



}
